#include "stats.h"

//-----------------------------------------------------------------------------
Stats::Stats()
    : m_lines{0}
    , m_score{0}
{}

//-----------------------------------------------------------------------------
void Stats::addLines(int lines)
{
    m_lines += lines;
}

//-----------------------------------------------------------------------------
void Stats::addScore(int score)
{
    m_score += score;
}

//-----------------------------------------------------------------------------
int Stats::score() const
{
    return m_score;
}

//-----------------------------------------------------------------------------
int Stats::lines() const
{
    return m_lines;
}
